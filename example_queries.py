QUERY_1 = '''
select
	l_shipmode,
	sum(case
		when o_orderpriority = '1-URGENT'
			or o_orderpriority = '2-HIGH'
			then 1
		else 0
	end) as high_line_count,
	sum(case
		when o_orderpriority <> '1-URGENT'
			and o_orderpriority <> '2-HIGH'
			then 1
		else 0
	end) as low_line_count
from
	lineitem
where
	l_shipmode in ('FOB', 'AIR')
group by
	l_shipmode
order by
	l_shipmode
LIMIT 1;
'''

